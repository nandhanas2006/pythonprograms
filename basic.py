import turtle as t
t.bgcolor("black")
t.fillcolor("blue")
t.pensize(8)
t.pendown()
t.color("white")
for i in range(3):
    t.fd(300)
    t.rt(-120)

t.fd(-200)

for i in range(3):
    t.fd(200)
    t.rt(-120)

t.fd(-100)

for i in range(3):
    t.fd(100)
    t.rt(-120)
t.done()
 

